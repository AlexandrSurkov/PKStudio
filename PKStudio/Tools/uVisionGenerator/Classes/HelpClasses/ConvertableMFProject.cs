using PKStudio.ItemWrappers;

namespace PKStudio.Tools.uVisionGenerator.Classes.HelpClasses
{
    public class ConvertableMFProject
    {
        public ConvertableMFProject(ProjectWrapper proj, string BuildType, string MediaType)
        {
            _Project = proj;
            _BuildType = BuildType;
            _MediaType = MediaType;
        }

        private ProjectWrapper _Project = null;
        private string _BuildType = string.Empty;
        private string _MediaType = string.Empty;

        public ProjectWrapper Project { get { return this._Project; } set { this._Project = value; } }
        public string BuildType { get { return this._BuildType; } set { this._BuildType = value; } }
        public string MediaType { get { return this._MediaType; } set { this._MediaType = value; } }
    }

}
